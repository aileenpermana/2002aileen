package entity;

/**
 * This enum represents the status of the application
 */
public enum ApplicationStatus {
    /**
     * The status of the application is pending
     */
    PENDING,
    /** 
     * The status of the application is successful
     */
    SUCCESSFUL,
    /**
     * The status of the application is unsuccessful
     */
    UNSUCCESSFUL,
    /**
     * The status of the application is booked
     */
    BOOKED
}